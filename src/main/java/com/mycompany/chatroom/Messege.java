package com.mycompany.chatroom;

import java.util.Date;

/**
 *
 * @author Omar
 */
class Messege {
    private String name;
    private String text;
    private long date;

    public Messege(String name, String text, long date) {
        this.name = name;
        this.text = text;
        this.date = date;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public long getDate() {
        return date;
    }

    public void setDate(long date) {
        this.date = date;
    }
    
}
